    void solve(){
        int T = in.nextInt();
        for(int cnt = 1; cnt <= T; cnt++){
            int N = in.nextInt();
            int[][] m = new int[N][N];
            int sum = 0;
            for(int i = 0; i < N; i++){
                for(int j = 0; j < N; j++){
                    m[i][j] = in.nextInt();
                    if(i == j) sum += m[i][j];
                }
            }
            boolean[] used = new boolean[N+1];
            int sumH = 0, sumW = 0;
            LOOP:for(int i = 0; i < N; i++){
                Arrays.fill(used, false);
                for(int j = 0; j < N; j++){
                    if(used[m[i][j]]){
                        sumH++;
                        continue LOOP;
                    }
                    used[m[i][j]] = true;
                }
            }
            LOOP:for(int i = 0; i < N; i++){
                Arrays.fill(used, false);
                for(int j = 0; j < N; j++){
                    if(used[m[j][i]]){
                        sumW++;
                        continue LOOP;
                    }
                    used[m[j][i]] = true;
                }
            }
            out.println("Case #" + cnt + ": " + sum + " " + sumH + " " + sumW);
        }
    }
    
