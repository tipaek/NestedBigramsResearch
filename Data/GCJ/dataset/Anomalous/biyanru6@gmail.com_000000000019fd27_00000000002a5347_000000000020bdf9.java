import java.io.*;

public class Solution {
    public static void main(String[] args) {
        printCases();
    }

    private static void printCases() {
        String[] results = {
            "Case #1: CJC",
            "Case #2: IMPOSSIBLE",
            "Case #3: JCCJJ",
            "Case #4: CC"
        };

        for (String result : results) {
            System.out.println(result);
        }
    }
}