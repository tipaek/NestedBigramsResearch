import java.io.*;
import java.util.*;
import java.lang.*;
public class Solution
{
    public static void main(String [] args)
    {
        Scanner sc=new Scanner (new BufferedReader(new InputStreamReader(System.in)));
        PrintWriter w=new PrintWriter(System.out);
        int test=sc.nextInt();
        w.println(test);
        w.close();
    }
    
}