import java.util.*;
class Solution {
	@SuppressWarnings({"resource"})
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int t = sc.nextInt();
		for(int i=1;i<=t;i++) {
			int n = sc.nextInt();
			System.out.println("Case #"+i+":");
			for(int j=1;j<=n;j++) {
				System.out.println(j+" "+1);
			}
		}
	}

}