import java.util.*;
public class Solution
{
    public static void main(String[] args)
    {
        Scanner in=new Scanner(System.in);
        int a,b,c,d,e,f,g,h,i,j,k;
        String s, t,u,v,w;
        a=in.nextInt();
        c=in.nextInt();
        for(b=1;b<=a;b++)
        {
              int[]z=new int[c];
              s="Y";
              System.out.println("1");
              System.out.flush();
              s=in.next();
              if(s=="N")
              break;
              f=-1;g=-1;h=i=j=0;
              for(d=0;d<149;d+=2)
              {
                     if(d%10==0&&d>0)
                     {
                            if(f>=0)
                            {
                                   System.out.println(f+1);
                                   System.out.flush();
                                    s=in.next();
                                   if(s=="N")break;
                                   e=s=="1"?1:0;
                                   if(z[f]!=e)
                                   {
                                         i=1;
                                   }
                            }
                            else
                            {
                                  System.out.println("1");
                                  System.out.flush();
                                   s=in.next();
                                   if(s=="N")break;
                                   e=s=="1"?1:0;
                            }
                            if(g>=0)
                            {
                                    System.out.println(g+1);
                                    System.out.flush();
                                    s=in.next();
                                   if(s=="N")break;
                                   e=s=="1"?1:0;
                                    if(z[g]!=e)
                                    {
                                          j=1;
                                    }
                            }
                            else
                            {
                                  System.out.println("1");
                                  System.out.flush();
                               s=in.next();
                                   if(s=="N")break;
                                   e=s=="1"?1:0;
                           }
                           d+=2;
                            if(i==1&&j==1)
                            {
                                  for(k=0;k<h;k++)
                                  {
                                          z[k]=z[k]==1?0:1;
                                          z[c-k-1]=z[c-k-1]==1?0:1;
                                 }
                           }
                           else if(i==1)
                           {
                                 for(k=0;k<h;k++)
                                 {
                                         if(z[k]==z[c-k-1])
                                               z[k]=z[c-k-1]=z[k]==0?1:0;
                                 }
                           }
                           else if(j==1)
                           {
                                 for(k=0;k<h;k++)
                                 {
                                       if(z[k]!=z[c-k-1])
                                       {
                                               z[k]=z[k]==0?1:0;
                                               z[c-k-1]=z[c-k-1]==0?1:0;
                                       }
                                  }
                            }
                     }
                     System.out.println(h+1);
                     System.out.flush();
                      s=in.next();
                                   if(s=="N")break;
                                   z[h]=s=="1"?1:0;
                     System.out.println(c-h);
                     System.out.flush();
                     s=in.next();
                                   if(s=="N")break;
                                   z[c-h-1]=s=="1"?1:0;
                     if(z[h]==z[c-h-1])
                           f=h;
                     else
                           g=h;
                     h++;
                     if(h==c/2)
                           break;
              }
              if(s=="N")
                break;
               t="";
              for(d=0;d<c;d++)
              {
                     t+=String.valueOf(z[d]);
              }
              System.out.println(t);
              System.out.flush();
              s=in.next();
              if(s=="N")
              break;
        }
    }
}